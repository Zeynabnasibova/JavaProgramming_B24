package w3resource.com.String;

public class _24ReplaceCharacter {

    public static void main(String[] args) {

        /*
        Write a Java program to replace a specified character with another character.

         */
        String str = "The quick brown fox jumps over the lazy dog.";

        // Replace all the 'd' characters with 'f' characters.
        String new_str = str.replace('d', 'f');

        // Display the strings for comparison.
        System.out.println("Original string: " + str);
        System.out.println("New ReplitTask.String: " + new_str);

        System.out.println(str.replace('d','f'));


    }
}
