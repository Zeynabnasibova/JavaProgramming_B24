package az_Grup;

public class swaptonumber {
    public static void main(String[] args) {
/*
        Write a Java program to swap two variables.
        x = 15;
        y = 20;
 */
        int x, y , z;
         x  = 15;
         y  = 20;
         z = x;
         x = y ;
         y = z;

        System.out.println("x = " + x + "\n" + "y = " + y);


    }
}
